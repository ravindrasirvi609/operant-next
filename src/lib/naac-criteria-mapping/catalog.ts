export type NaacMetricCatalogEntry = (typeof naacMetricCatalog)[number];
export type NaacManualMetricCatalogEntry = (typeof naacManualMetricCatalog)[number];

export function getNaacCriterionMeta(criteriaCode: string) {
    return (
        naacCriterionCatalog.find((item) => item.criteriaCode === criteriaCode) ??
        null
    );
}

export function getNaacMetricMeta(
    tableName: string,
    fieldReference: string
): NaacMetricCatalogEntry | null {
    return (
        naacMetricCatalog.find(
            (item) =>
                item.tableName === tableName &&
                item.fieldReference === fieldReference
        ) ?? null
    );
}

export const defaultNaacCriteriaMappings = naacMetricCatalog.map((item) => ({
    criteriaCode: item.criteriaCode,
    criteriaName: item.criteriaName,
    tableName: item.tableName,
    fieldReference: item.fieldReference,
    weightage: item.defaultWeightage,
}));
